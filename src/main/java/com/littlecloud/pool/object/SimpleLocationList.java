package com.littlecloud.pool.object;

import java.io.Serializable;

public class SimpleLocationList implements Serializable
{
	private Long timestamp;
	private Float la;
	private Float lo;
	private Float at;
	private Float sp;
	private Float hu;
	private Float vu;
	private Float st;
	private Integer flag;
	private Float hd;
	public Long getTimestamp() {
		return timestamp;
	}
	public void setTimestamp(Long timestamp) {
		this.timestamp = timestamp;
	}
	public Float getLa() {
		return la;
	}
	public void setLa(Float la) {
		this.la = la;
	}
	public Float getLo() {
		return lo;
	}
	public void setLo(Float lo) {
		this.lo = lo;
	}
	public Float getAt() {
		return at;
	}
	public void setAt(Float at) {
		this.at = at;
	}
	public Float getSp() {
		return sp;
	}
	public void setSp(Float sp) {
		this.sp = sp;
	}
	public Float getHu() {
		return hu;
	}
	public void setHu(Float hu) {
		this.hu = hu;
	}
	public Float getVu() {
		return vu;
	}
	public void setVu(Float vu) {
		this.vu = vu;
	}
	public Float getSt() {
		return st;
	}
	public void setSt(Float st) {
		this.st = st;
	}
	public Integer getFlag() {
		return flag;
	}
	public void setFlag(Integer flag) {
		this.flag = flag;
	}
	public Float getHd() {
		return hd;
	}
	public void setHd(Float hd) {
		this.hd = hd;
	}
	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("SimpleLocationList [timestamp=");
		builder.append(timestamp);
		builder.append(", la=");
		builder.append(la);
		builder.append(", lo=");
		builder.append(lo);
		builder.append(", at=");
		builder.append(at);
		builder.append(", sp=");
		builder.append(sp);
		builder.append(", hu=");
		builder.append(hu);
		builder.append(", vu=");
		builder.append(vu);
		builder.append(", st=");
		builder.append(st);
		builder.append(", flag=");
		builder.append(flag);
		builder.append(", hd=");
		builder.append(hd);
		builder.append("]");
		return builder.toString();
	}
	
}
