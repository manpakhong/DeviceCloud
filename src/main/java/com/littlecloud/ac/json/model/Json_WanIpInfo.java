package com.littlecloud.ac.json.model;

public class Json_WanIpInfo {
	private Integer id;
	private Integer connected;
	private String name;
	private String ip;
	
	public Json_WanIpInfo(Integer id, Integer connected, String name, String ip) {
		super();
		this.id = id;
		this.connected = connected;
		this.name = name;
		this.ip = ip;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getConnected() {
		return connected;
	}
	public void setConnected(Integer connected) {
		this.connected = connected;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	
	@Override
	public String toString() {
		return "Json_WanIpInfo [id=" + id + ", connected=" + connected
				+ ", name=" + name + ", ip=" + ip + "]";
	}
}
