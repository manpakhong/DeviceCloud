package com.littlecloud.ac.health.info;

public class UnHealthInfo 
{
	String status;
	String unhealthy_reason;
	boolean isHealthy;
	boolean isStartup;
	
	public boolean isHealthy() {
		return isHealthy;
	}
	public void setHealthy(boolean isHealthy) {
		this.isHealthy = isHealthy;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getUnhealthy_reason() {
		return unhealthy_reason;
	}
	public void setUnhealthy_reason(String unhealthy_reason) {
		this.unhealthy_reason = unhealthy_reason;
	}
	public boolean isStartup() {
		return isStartup;
	}
	public void setStartup(boolean isStartup) {
		this.isStartup = isStartup;
	}
}
