package com.littlecloud.control.entity;

import com.google.gson.annotations.SerializedName;

public class CaptivePortalExtraSettings {
	@SerializedName("id") String id = null;
	@SerializedName("message") String message = null;
	@SerializedName("last_updated") String lastUpdated = null;
	@SerializedName("scope") String scope = null;

	public CaptivePortalExtraSettings() {

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getLastUpdated() {
		return lastUpdated;
	}

	public void setLastUpdated(String lastUpdated) {
		this.lastUpdated = lastUpdated;
	}

	public String getScope() {
		return scope;
	}

	public void setScope(String scope) {
		this.scope = scope;
	}
}
