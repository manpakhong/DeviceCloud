package com.littlecloud.control.entity;

import com.google.gson.annotations.SerializedName;

public class ConfigurationSettingsExtraSettings {
	@SerializedName("wechat_app_id") String wechatAppId;
	@SerializedName("wechat_app_secret") String wechatAppSecret;
	@SerializedName("wechat_oid") String wechatOid;
	@SerializedName("wechat_access_button") String wechatAccessButton;
	
	public ConfigurationSettingsExtraSettings(){}

	public String getWechatAppId() {
		return wechatAppId;
	}

	public void setWechatAppId(String wechatAppId) {
		this.wechatAppId = wechatAppId;
	}

	public String getWechatAppSecret() {
		return wechatAppSecret;
	}

	public void setWechatAppSecret(String wechatAppSecret) {
		this.wechatAppSecret = wechatAppSecret;
	}

	public String getWechatOid() {
		return wechatOid;
	}

	public void setWechatOid(String wechatOid) {
		this.wechatOid = wechatOid;
	}

	public String getWechatAccessButton() {
		return wechatAccessButton;
	}

	public void setWechatAccessButton(String wechatAccessButton) {
		this.wechatAccessButton = wechatAccessButton;
	}
}
