package com.littlecloud.control.json.model.pepvpn;

import com.google.gson.annotations.SerializedName;

public class Peer_Networks {
	Integer id; // network id
	String name;
	@SerializedName("endpoint_count")
	Long endpointz;

	public Peer_Networks(Integer id, String name, Long endpointz) {
		super();
		this.id = id;
		this.name = name;
		this.endpointz = endpointz;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getEndpointz() {
		return endpointz;
	}

	public void setEndpointz(Long endpointz) {
		this.endpointz = endpointz;
	}
}