package com.littlecloud.control.json.model.pepvpn;

import java.io.Serializable;
import java.util.ArrayList;

public class PeerDetailResponse implements Serializable {
	private ArrayList<PeerDetail> peer;
	//private ArrayList<Integer> order;
	
	public ArrayList<PeerDetail> getPeer() {
		return peer;
	}
	public void setPeer(ArrayList<PeerDetail> peer) {
		this.peer = peer;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("{peer=");
		builder.append(peer);
		builder.append("}");
		return builder.toString();
	}
}