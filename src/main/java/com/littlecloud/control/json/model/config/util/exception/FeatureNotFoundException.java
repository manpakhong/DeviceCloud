package com.littlecloud.control.json.model.config.util.exception;

/* This exception occurs when device feature is not received from device */ 
public class FeatureNotFoundException extends Exception {
	
	public FeatureNotFoundException(String message) {
		super(message);
	}

	public FeatureNotFoundException(String message, Throwable throwable) {
		super(message, throwable);
	}
}
