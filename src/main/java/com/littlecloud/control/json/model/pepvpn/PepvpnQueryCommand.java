package com.littlecloud.control.json.model.pepvpn;

import java.util.ArrayList;

public class PepvpnQueryCommand {

	private int version;
	private ArrayList<String> sn;
	private ArrayList<String> tunnel;
	
	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("PepvpnQueryCommand [version=");
		builder.append(version);
		builder.append(", sn=");
		builder.append(sn);
		builder.append(", tunnel=");
		builder.append(tunnel);
		builder.append("]");
		return builder.toString();
	}
	public int getVersion() {
		return version;
	}
	public void setVersion(int version) {
		this.version = version;
	}
	public ArrayList<String> getSn() {
		return sn;
	}
	public void setSn(ArrayList<String> sn) {
		this.sn = sn;
	}
	public ArrayList<String> getTunnel() {
		return tunnel;
	}
	public void setTunnel(ArrayList<String> tunnel) {
		this.tunnel = tunnel;
	}
}
