package com.littlecloud.control.json.model.config;

import java.util.ArrayList;
import java.util.List;

public class JsonConf_RadioSupport 
{
	private List<FeatureRadioConfig> featureRadioConfigLst;
	private Boolean Refreshing;
	
	public List<FeatureRadioConfig> getFeatureRadioConfigLst() {
		return featureRadioConfigLst;
	}

	public void setFeatureRadioConfigLst(
			List<FeatureRadioConfig> featureRadioConfigLst) {
		this.featureRadioConfigLst = featureRadioConfigLst;
	}

	public Boolean getRefreshing() {
		return Refreshing;
	}

	public void setRefreshing(Boolean refreshing) {
		Refreshing = refreshing;
	}

	@Override
	public String toString() {
		return "JsonConf_RadioSupport [featureRadioConfigLst="
				+ featureRadioConfigLst + ", Refreshing=" + Refreshing + "]";
	}

	public class FeatureRadioConfig
	{
		private Integer module_id;
		private String frequency_band;
		
		public Integer getModule_id() {
			return module_id;
		}
		public void setModule_id(Integer module_id) {
			this.module_id = module_id;
		}
		public String getFrequency_band() {
			return frequency_band;
		}
		public void setFrequency_band(String frequency_band) {
			this.frequency_band = frequency_band;
		}
		@Override
		public String toString() {
			return "FeatureRadioConfig [module_id=" + module_id
					+ ", frequency_band=" + frequency_band + "]";
		}
	}
	
}
