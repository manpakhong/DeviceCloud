package com.littlecloud.control.json.model;

import java.util.List;

public class Json_Organizations {

	private Integer network_id;
	private String organization_id;
	private Integer hub_count;
	private List<Json_Networks> networks;
	private String features;

	public Integer getNetwork_id() {
		return network_id;
	}

	public void setNetwork_id(Integer network_id) {
		this.network_id = network_id;
	}

	public Integer getHub_count() {
		return hub_count;
	}

	public void setHub_count(Integer hub_count) {
		this.hub_count = hub_count;
	}

	public List<Json_Networks> getNetworks() {
		return networks;
	}

	public void setNetworks(List<Json_Networks> networks) {
		this.networks = networks;
	}

	public String getOrganization_id() {
		return organization_id;
	}

	public void setOrganization_id(String organization_id) {
		this.organization_id = organization_id;
	}

	public String getFeatures() {
		return features;
	}

	public void setFeatures(String features) {
		this.features = features;
	}
}
