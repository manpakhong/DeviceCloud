package com.littlecloud.fusionhub;

public class Json_FusionHubLicenseRequest {

	private String key_code;
	private String vm_id;
	private String host_id;
	private String sn;

	public Json_FusionHubLicenseRequest(String key_code, String vm_id, String host_id, String sn) {
		super();
		this.key_code = key_code;
		this.vm_id = vm_id;
		this.host_id = host_id;
		this.sn = sn;
	}

	public String getKey_code() {
		return key_code;
	}

	public void setKey_code(String key_code) {
		this.key_code = key_code;
	}

	public String getVm_id() {
		return vm_id;
	}

	public void setVm_id(String vm_id) {
		this.vm_id = vm_id;
	}

	public String getHost_id() {
		return host_id;
	}

	public void setHost_id(String host_id) {
		this.host_id = host_id;
	}

	public String getSn() {
		return sn;
	}

	public void setSn(String sn) {
		this.sn = sn;
	}

	@Override
	public String toString() {
		return "Json_FusionHubLicenseRequest [key_code=" + key_code + ", vm_id=" + vm_id + ", host_id=" + host_id + ", sn=" + sn + "]";
	}
}
